//บวก
function add(a, b){
    return a + b;
}
let result = add(10, 5);
console.log(result);
//ลบ
function subtract (numA, numB){
    return numA - numB;
}
let resultSubtract = subtract(10, 5);
console.log(resultSubtract);
//คูณ
function multiply(mulA, mulB){
    return mulA * mulB;
}
console.log(multiply(10, 5));
//หาร
function divide (divA, divB){
    return divA / divB;
}
console.log(divide(10, 5));
